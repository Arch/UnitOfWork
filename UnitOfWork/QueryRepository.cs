        public virtual IQueryable<TEntity> FromSql(string sql, params object[] parameters) => _dbQuery.FromSql(sql, parameters);

        /// <summary>
        /// Gets the count based on a predicate.
        /// </summary>
        /// <param name="predicate"></param>
        /// <returns></returns>
        public virtual int Count(Expression<Func<TEntity, bool>> predicate = null)
        {
